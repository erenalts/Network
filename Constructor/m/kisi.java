package m;

	public class kisi{
		String isim;
		String soyisim;
		int yas;
		
		public kisi() { //Parametreli metot
		
		}
		public kisi(String i) { //Parametreli metot
			isim = i;
		}
		
		public kisi(String i, String s) { //Parametresiz metot
			isim = i;
			soyisim = s;
		}
		
		public kisi(String i, String s, int y) { //Parametresiz metot
			isim = i;
			soyisim = s;
			yas = y;
		}
	}
	
